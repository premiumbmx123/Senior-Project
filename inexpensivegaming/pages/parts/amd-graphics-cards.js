export async function getServerSideProps() {
  const { db } = await connectToDatabase();

  const parts = await db
    .collection("graphicsCards")
    .find({"manufacturer": "AMD"})
    .toArray();

  return {
    props: {
      parts: JSON.parse(JSON.stringify(parts)),
    },
  };
}

